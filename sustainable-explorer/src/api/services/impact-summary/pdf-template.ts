/** Pill grid (chunked 3-per-row) + "N of 17 SDGs addressed" footer, from `summary.sdgContributions` (already sorted by SDG number). */
function buildSdgSection(summary: ImpactSummaryResponseDto): Content[] {
    const sdgs = summary.sdgContributions;
    if (sdgs.length === 0) {
        return [{ text: 'No SDG contributions recorded.', style: 'footerNote' }];
    }

    const topGoal = [...sdgs].sort((a, b) => b.credits - a.credits || b.projectCount - a.projectCount)[0];
    const rows = chunk(sdgs, SDG_PILLS_PER_ROW).map((row) => ({ columns: row.map(sdgPill), columnGap: 6 }) as Content);

    return [
        ...rows,
        {
            text: `${sdgs.length} of 17 SDG addressed · Top goal: SDG ${topGoal.sdgId} ${topGoal.name} (${topGoal.projectCount} projects)`,
            style: 'footerNote',
            margin: [0, 6, 0, 0],
        },
    ];
}

