/** Splits an array into fixed-size chunks (used to lay out the SDG pill grid in rows). */
function chunk<T>(arr: T[], size: number): T[][] {
    const out: T[][] = [];
    for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
    return out;
}

/** `"did:hedera:mainnet:34VqT5em9k..."` — truncates a long DID for a footer caption. `null`/`undefined` renders "--". */
function truncateDid(did: string | null | undefined, visibleChars = 28): string {
    if (!did) return '--';
    if (did.length <= visibleChars + 3) return did;
    return `${did.slice(0, visibleChars)}...`;
}

/**
 * Picks one representative registry DID for the "Registry DID" captions: the scope's named registry if it
 * matches a row in `registryBreakdown`, else the first row that has a DID, else `null`. Never fabricates a DID.
 */
function pickRegistryDid(summary: ImpactSummaryResponseDto, scope: ImpactSummaryReportScope | undefined): string | null {
    if (scope?.registryLabel) {
        const match = summary.registryBreakdown.find((r) => r.displayName === scope.registryLabel && r.registryDid);
        if (match) return match.registryDid;
    }
    return summary.registryBreakdown.find((r) => r.registryDid)?.registryDid ?? null;
}

/** Section heading, matching the report's H2-style headings. */
function sectionHeader(text: string): Content {
    return { text, style: 'sectionHeader' };
}

/** Shaded content card — same `fillColor` single-cell-table trick as `metricCard()`/`badge()` above. */
function card(content: Content[]): Content {
    return {
        table: {
            widths: ['*'],
            body: [[{ stack: content, fillColor: COLORS.cardBg, border: [false, false, false, false], margin: [12, 12, 12, 12] }]],
        },
        layout: 'noBorders',
    };
}

/** Two cards stacked full-width rather than side-by-side, since the inner bar canvases are wider than a half-page column and would clip in a `columns` layout. */
function twoColumnRow(left: Content, right: Content): Content {
    return { stack: [left, { text: '', margin: [0, 3, 0, 0] }, right], margin: [0, 4, 0, 0] };
}

const BAR_WIDTH = 190;
const BAR_HEIGHT = 8;

/** One horizontal bar row: `label | ▇▇▇▇▇____ | value`. Used for sector/geo breakdowns. */
function barRow(label: string, percentage: number, valueLabel: string, color: string): Content {
    const filled = percentage > 0 ? Math.max(3, (percentage / 100) * BAR_WIDTH) : 0;
    return {
        columns: [
            { width: 95, text: label, style: 'barLabel' },
            {
                width: BAR_WIDTH,
                canvas: [
                    { type: 'rect', x: 0, y: 1, w: BAR_WIDTH, h: BAR_HEIGHT, color: COLORS.border },
                    { type: 'rect', x: 0, y: 1, w: filled, h: BAR_HEIGHT, color },
                ],
            },
            { width: '*', text: valueLabel, style: 'barValue', alignment: 'right' },
        ],
        columnGap: 8,
        margin: [0, 3, 0, 3],
    };
}

/** Small colored square + label, used for the Credit Lifecycle legend. */
function legendSwatch(color: string, label: string): Content {
    return {
        columns: [
            { width: 10, canvas: [{ type: 'rect', x: 0, y: 2, w: 8, h: 8, color }] },
            { width: 'auto', text: label, style: 'barValue' },
        ],
        columnGap: 4,
    };
}

function tableHeaderCell(text: string, alignment: 'left' | 'center' | 'right' = 'left'): Content {
    return { text, style: 'tableHeaderText', alignment, fillColor: COLORS.greyBg, margin: [0, 4, 0, 4] };
}

function tableCell(text: string, alignment: 'left' | 'center' | 'right' = 'left'): Content {
    return { text, style: 'tableCellText', alignment };
}

