/** Registry, Projects, Issuances, Credits tCO2e. `registryBreakdown` carries project/issuance/policy counts but no per-registry credit total, so the Credits column always renders "--". */
function buildRegistryTable(summary: ImpactSummaryResponseDto): Content {
    const rows = summary.registryBreakdown;
    const body: Content[][] = [
        [tableHeaderCell('Registry'), tableHeaderCell('Projects', 'center'), tableHeaderCell('Issuances', 'right'), tableHeaderCell('Credits tCO2e', 'right')],
        ...rows.map((r) => [
            tableCell(r.displayName ?? truncateDid(r.registryDid)),
            tableCell(String(r.projectCount), 'center'),
            tableCell(String(r.issuanceCount), 'right'),
            tableCell('--', 'right'),
        ]),
    ];

    return card([
        { text: 'Credits by Registry', style: 'cardTitle' },
        rows.length > 0
            ? { table: { headerRows: 1, widths: ['*', 'auto', 'auto', 'auto'], body }, layout: 'lightHorizontalLines' }
            : { text: 'No registry data available.', style: 'footerNote' },
    ]);
}

